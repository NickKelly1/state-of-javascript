export const msToSecond = (ms: number) => ms / (1000);
export const msToMinute = (ms: number) => ms / (1000 * 60);
export const msToHour = (ms: number) => ms / (1000 * 60 * 60);
export const msToDay = (ms: number) => ms / (1000 * 60 * 60 * 24);
export const msToWeek = (ms: number) => ms / (1000 * 60 * 60 * 24 * 7);